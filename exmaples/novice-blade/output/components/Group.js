import { createElement } from 'novice'

export function getDefaultState() {
  return {
    style: {},
    center: false,
  }
}

export function render({ state, children }) {
  const style = {
    ...state.style,
  }

  if (state.center) {
    Object.assign(style, {
      position: 'relative',
      marginLeft: 'auto',
      marginRight: 'auto',
    })
  }

  return <div style={style}>{children}</div>
}
